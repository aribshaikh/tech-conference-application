package NewUI;

/**
 * This class contains a presenter method landing menu, which shows the user what options are allowed once
 * they run the program.
 */
public class LandingMenu {
    /**
     * Prints the options a user can do once program is run
     */
    public void landingmenu() {
        System.out.println("Conference System\n");
        System.out.println("1: Log in");
        System.out.println("2: Sign up");
        System.out.println("3: Event Search Engine");
        System.out.println("\n0: Quit");
    }
}
